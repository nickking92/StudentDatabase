    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try 
            {
                ResultSet rs=DBConnection.adminLogin(jTextField1.getText(),jPasswordField1.getText());
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(null,"Welcome " +jTextField1.getText() );
                    RegisterAdmin al=new RegisterAdmin();
                    al.setVisible(true);
                    this.setVisible(false);
                
                }else if(jTextField1.getText().equals("")){
                    jTextField1.requestFocus();
                    
                    JOptionPane.showMessageDialog(null,"username field is empty");
                    jLabel1.setText("Note:Only admin can access this data");
                  
                }else if(jPasswordField1.getText().equals("")){
                    jPasswordField1.requestFocus();
                    JOptionPane.showMessageDialog(null,"password field is empty");
                    jLabel1.setText("Note:Only admin can access this data");
                }else{
                    JOptionPane.showMessageDialog(rootPane,"wrong username or password");
                    jLabel1.setText("Note:Only admin can access this data");
                }
                        
                        
                    
                
            }catch (SQLException ex) 
            {
                    JOptionPane.showMessageDialog(null,"Connection Error");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

